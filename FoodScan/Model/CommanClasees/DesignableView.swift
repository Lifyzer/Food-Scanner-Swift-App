    var Color: UIColor? {
        get {

            if let color = layer.backgroundColor {
                return UIColor(cgColor: color)
            }
            return nil
        }
        set {
            if let color = newValue {
                layer.backgroundColor = color.cgColor
            } else {
                layer.borderColor = nil
            }
        }
    }
}


